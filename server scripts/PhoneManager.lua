local interactiveFolder = workspace:FindFirstChild("level_1") and workspace.level_1:FindFirstChild("interactive")
if not interactiveFolder then
	warn("–ü–∞–ø–∫–∞ workspace.level_1.interactive –Ω–µ –Ω–∞–π–¥–µ–Ω–∞!")
	return
