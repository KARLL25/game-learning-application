local function onTriggered(player, phone)
	local data = phoneData[phone.Name]
	if data then
		print("–¢–µ–ª–µ—Ñ–æ–Ω –∞–∫—Ç–∏–≤–∏—Ä–æ–≤–∞–Ω: " .. phone.Name)
		phonesFound.Value = phonesFound.Value + 1
		phoneEvent:FireClient(player, nil, data.title, data.content, data.tasks, phone.Name)
		local prompt = phone:FindFirstChild("ProximityPrompt")
		if prompt then
			prompt.Enabled = false
			print("ProximityPrompt –æ—Ç–∫–ª—é—á—ë–Ω –¥–ª—è: " .. phone.Name)
		end
		
		phone:Destroy()
		print("–¢–µ–ª–µ—Ñ–æ–Ω —É–¥–∞–ª—ë–Ω: " .. phone.Name)
	else
		warn("–î–∞–Ω–Ω—ã–µ –Ω–µ –Ω–∞–π–¥–µ–Ω—ã –¥–ª—è —Ç–µ–ª–µ—Ñ–æ–Ω–∞: " .. phone.Name .. " (–ò–º—è –æ–±—ä–µ–∫—Ç–∞: " .. tostring(phone.Name) .. ")")
	end
end

