print("–ù–∞—á–∏–Ω–∞–µ–º –ø—Ä–æ–≤–µ—Ä–∫—É —Ç–µ–ª–µ—Ñ–æ–Ω–æ–≤ –≤ " .. interactiveFolder.Name)
for _, phone in pairs(interactiveFolder:GetChildren()) do
	print("–ü—Ä–æ–≤–µ—Ä—è–µ–º –æ–±—ä–µ–∫—Ç: " .. phone.Name .. " (–¢–∏–ø: " .. phone.ClassName .. ")")
	-- –ü—Ä–æ–≤–µ—Ä—è–µ–º, —á—Ç–æ –∏–º—è –Ω–∞—á–∏–Ω–∞–µ—Ç—Å—è —Å Phone (–∏–≥–Ω–æ—Ä–∏—Ä—É–µ–º —Ä–µ–≥–∏—Å—Ç—Ä –∏ –ø—Ä–æ–±–µ–ª—ã)
	local normalizedName = phone.Name:lower():match("^phone[_%s]*%d+")
	if not normalizedName then
		warn("–û–±—ä–µ–∫—Ç –Ω–µ —è–≤–ª—è–µ—Ç—Å—è —Ç–µ–ª–µ—Ñ–æ–Ω–æ–º (–Ω–µ–≤–µ—Ä–Ω–æ–µ –∏–º—è): " .. phone.Name)
		continue
	end
	local prompt = phone:FindFirstChild("ProximityPrompt")
	if prompt then
		print("–ù–∞–π–¥–µ–Ω ProximityPrompt –≤: " .. phone.Name)
		
		if not prompt.Enabled then
			warn("ProximityPrompt –∏–∑–Ω–∞—á–∞–ª—å–Ω–æ –æ—Ç–∫–ª—é—á—ë–Ω –¥–ª—è: " .. phone.Name)
